def get_visual_analysis_prompt(persona_name: str = None) -> str:
    """
    Generate complete prompt for visual analysis

    Args:
        persona_name: Optional persona context (student, applicant, teacher)

    Returns:
        Complete formatted prompt
    """
    persona_context = ""
    if persona_name:
        persona_map = {
            "student": "ПЕРСОНА: Студент - активный пользователь, ищет расписание и учебные материалы, ограничен во времени.",
            "applicant": "ПЕРСОНА: Абитуриент - новичок на сайте, ищет информацию о поступлении, тревожится о дедлайнах.",
            "teacher": "ПЕРСОНА: Преподаватель - опытный пользователь, нужен быстрый доступ к курсам и оценкам студентов."
        }
        persona_context = f"\n\n{persona_map.get(persona_name, '')}\n"

    prompt = f"""{VISUAL_ANALYSIS_SYSTEM_PROMPT}
{persona_context}
{HEURISTICS_DESCRIPTION}

{SEVERITY_GUIDELINES}

{ANALYSIS_INSTRUCTIONS}

{OUTPUT_FORMAT}
"""

    return prompt
